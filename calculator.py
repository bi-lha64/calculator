def add(a,b):
	c=a+b
	print(c)

def multiply(b,t):
	f=b*t
	print(f)

def subtraction(q,t):
	k=q-t
	print(k)

def divide(l,m):
	j=l/m
	print(j)

def modulus(d,h):
	p=d%h
	print(p)
def whole_number_division(s,g):
	q=s//g
	print(q)

